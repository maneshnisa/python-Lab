# Assignment Operators

a = 5
add, sub, mul, div, rem, expo = 0, 0, 0, 1, 1, 1 # Multiple variable assignment

add += a # same as add = 0 + 5.
# add = add + a
print(add)

sub -= a # same as sub = 0 - 5.
print(sub)

mul *= a # same as mul = 0 * 5.
print(mul)

div /= a # same as div = 1 / 5.
print(div)

rem %= a # same as rem = 1 % 5.
print(rem)

expo **= a # same as expo = 1 ** 5
print(expo)


